/**
 * Η κλαση InitialsApp εμφανίζει τα γράμματα Σ και Π.
 */
public class InitialsApp {
    /**
     * Εκτυπωνει τα 2 πρωτα αρχικα γραμματα του ονοματος με *.
     * @param args δεν χρησιμοποιουνται
     */
    public static void main(String[] args) {
        System.out.println("""
                ********           **************
                *                  *            *
                  *                *            *
                    *              *            *
                  *                *            *
                *                  *            *
                ********           *            *
                """);
    }
}
